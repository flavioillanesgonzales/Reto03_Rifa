
package modelo;


public class Rifa {
    private String CODRIF;
    private String IDPAR;
    private String NOMPAR;
    private String APEPAR;
    private String CELULAR;
    private String RESULTADO;
    
    private String cantidad;
    public String getCODRIF() {
        return CODRIF;
    }

    public void setCODRIF(String CODRIF) {
        this.CODRIF = CODRIF;
    }

    public String getIDPAR() {
        return IDPAR;
    }

    public void setIDPAR(String IDPAR) {
        this.IDPAR = IDPAR;
    }

    public String getCantidad() {
        return cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    public String getNOMPAR() {
        return NOMPAR;
    }

    public void setNOMPAR(String NOMPAR) {
        this.NOMPAR = NOMPAR;
    }

    public String getAPEPAR() {
        return APEPAR;
    }

    public void setAPEPAR(String APEPAR) {
        this.APEPAR = APEPAR;
    }

    public String getCELULAR() {
        return CELULAR;
    }

    public void setCELULAR(String CELULAR) {
        this.CELULAR = CELULAR;
    }

    public String getRESULTADO() {
        return RESULTADO;
    }

    public void setRESULTADO(String RESULTADO) {
        this.RESULTADO = RESULTADO;
    }


    
    
}
